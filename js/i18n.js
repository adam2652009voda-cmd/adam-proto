function getNestedValue(obj, path) {
  return path.split('.').reduce((acc, key) => acc?.[key], obj);
}

function applyLanguage(lang) {
  const t = translations[lang];
  if (!t) return;

  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

  document.title = t.meta.title;
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) metaDesc.content = t.meta.description;

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.dataset.i18n;
    const value = getNestedValue(t, key);
    if (value !== undefined) el.textContent = value;
  });

  document.querySelectorAll('[data-i18n-html]').forEach((el) => {
    const key = el.dataset.i18nHtml;
    const value = getNestedValue(t, key);
    if (value !== undefined) el.innerHTML = value;
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.dataset.i18nPlaceholder;
    const value = getNestedValue(t, key);
    if (value !== undefined) el.placeholder = value;
  });

  document.querySelectorAll('[data-i18n-aria]').forEach((el) => {
    const key = el.dataset.i18nAria;
    const value = getNestedValue(t, key);
    if (value !== undefined) el.setAttribute('aria-label', value);
  });

  document.querySelectorAll('[data-i18n-alt]').forEach((el) => {
    const key = el.dataset.i18nAlt;
    const value = getNestedValue(t, key);
    if (value !== undefined) el.alt = value;
  });

  const skillTags = document.querySelectorAll('[data-i18n-tags]');
  skillTags.forEach((container) => {
    const tags = t.skills.tags;
    container.querySelectorAll('.skill-tag').forEach((tag, i) => {
      if (tags[i]) tag.textContent = tags[i];
    });
  });

  const langBtn = document.getElementById('lang-toggle');
  if (langBtn) {
    langBtn.textContent = lang === 'ar' ? 'EN' : 'ع';
    langBtn.setAttribute('aria-label', t.nav.toggleLang);
    langBtn.setAttribute('title', t.nav.toggleLang);
  }

  const themeToggle = document.getElementById('theme-toggle');
  if (themeToggle) themeToggle.setAttribute('aria-label', t.nav.toggleTheme);

  const navToggle = document.getElementById('nav-toggle');
  if (navToggle) navToggle.setAttribute('aria-label', t.nav.toggleMenu);

  localStorage.setItem('lang', lang);
  window.currentLang = lang;
}

window.translations = translations;
window.applyLanguage = applyLanguage;
